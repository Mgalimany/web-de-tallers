create table public.workshop_review_stats (
  workshop_id uuid primary key references public.workshops(id) on delete cascade,
  review_count integer not null default 0,
  verified_review_count integer not null default 0,
  avg_service numeric(4,2),
  avg_treatment numeric(4,2),
  avg_knowledge numeric(4,2),
  avg_price_value numeric(4,2),
  avg_would_return numeric(4,2),
  avg_overall_score numeric(4,2),
  bayesian_score numeric(4,2),
  updated_at timestamptz not null default now()
);

create or replace function public.refresh_workshop_review_stats(p_workshop_id uuid)
returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  prior_avg constant numeric := 7.00;
  prior_weight constant numeric := 5.00;
begin
  insert into public.workshop_review_stats (
    workshop_id,
    review_count,
    verified_review_count,
    avg_service,
    avg_treatment,
    avg_knowledge,
    avg_price_value,
    avg_would_return,
    avg_overall_score,
    bayesian_score,
    updated_at
  )
  select
    p_workshop_id,
    count(*)::integer,
    count(*) filter (where verified_visit)::integer,
    round(avg(score_service)::numeric, 2),
    round(avg(score_treatment)::numeric, 2),
    round(avg(score_knowledge)::numeric, 2),
    round(avg(score_price_value)::numeric, 2),
    round(avg(score_would_return)::numeric, 2),
    round(avg(overall_score)::numeric, 2),
    case
      when count(*) = 0 then null
      else round(((avg(overall_score) * count(*)) + (prior_avg * prior_weight)) / (count(*) + prior_weight), 2)
    end,
    now()
  from public.reviews
  where workshop_id = p_workshop_id
    and status = 'published'
  on conflict (workshop_id) do update
  set
    review_count = excluded.review_count,
    verified_review_count = excluded.verified_review_count,
    avg_service = excluded.avg_service,
    avg_treatment = excluded.avg_treatment,
    avg_knowledge = excluded.avg_knowledge,
    avg_price_value = excluded.avg_price_value,
    avg_would_return = excluded.avg_would_return,
    avg_overall_score = excluded.avg_overall_score,
    bayesian_score = excluded.bayesian_score,
    updated_at = now();
end;
$$;

create or replace function public.reviews_refresh_workshop_stats()
returns trigger
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  if tg_op in ('UPDATE', 'DELETE') then
    perform public.refresh_workshop_review_stats(old.workshop_id);
  end if;

  if tg_op in ('INSERT', 'UPDATE') then
    perform public.refresh_workshop_review_stats(new.workshop_id);
  end if;

  return null;
end;
$$;

create trigger reviews_refresh_workshop_stats_trigger
after insert or update or delete on public.reviews
for each row execute function public.reviews_refresh_workshop_stats();

-- Public RPC for map/list search. RLS still applies to the underlying tables.
create or replace function public.search_workshops(
  p_lat double precision,
  p_lng double precision,
  p_radius_m integer default 25000,
  p_make_id bigint default null,
  p_service_code text default null,
  p_max_hourly_rate_eur numeric default null,
  p_min_bayesian_score numeric default null,
  p_limit integer default 50,
  p_offset integer default 0
)
returns table (
  id uuid,
  name text,
  slug text,
  address_line text,
  municipality text,
  province text,
  latitude double precision,
  longitude double precision,
  distance_m double precision,
  hourly_rate_eur numeric,
  verified boolean,
  dgt_libro_taller boolean,
  review_count integer,
  avg_overall_score numeric,
  bayesian_score numeric,
  brand_match boolean
)
language sql
stable
as $$
  with query_point as (
    select extensions.st_setsrid(extensions.st_makepoint(p_lng, p_lat), 4326)::extensions.geography as location
  )
  select
    w.id,
    w.name,
    w.slug,
    w.address_line,
    w.municipality,
    w.province,
    extensions.st_y(w.location::extensions.geometry) as latitude,
    extensions.st_x(w.location::extensions.geometry) as longitude,
    extensions.st_distance(w.location, qp.location) as distance_m,
    w.hourly_rate_eur,
    w.verified,
    w.dgt_libro_taller,
    coalesce(s.review_count, 0) as review_count,
    s.avg_overall_score,
    s.bayesian_score,
    exists (
      select 1
      from public.workshop_brand_specializations wbs
      where wbs.workshop_id = w.id
        and wbs.make_id = p_make_id
    ) as brand_match
  from public.workshops w
  cross join query_point qp
  left join public.workshop_review_stats s on s.workshop_id = w.id
  where w.status = 'published'
    and extensions.st_dwithin(
      w.location,
      qp.location,
      least(greatest(coalesce(p_radius_m, 25000), 100), 200000)
    )
    and (
      p_make_id is null
      or exists (
        select 1
        from public.workshop_brand_specializations wbs
        where wbs.workshop_id = w.id
          and wbs.make_id = p_make_id
      )
    )
    and (
      p_service_code is null
      or exists (
        select 1
        from public.workshop_services ws
        join public.service_categories sc on sc.id = ws.service_category_id
        where ws.workshop_id = w.id
          and sc.code = p_service_code
      )
    )
    and (p_max_hourly_rate_eur is null or w.hourly_rate_eur <= p_max_hourly_rate_eur)
    and (p_min_bayesian_score is null or s.bayesian_score >= p_min_bayesian_score)
  order by
    exists (
      select 1
      from public.workshop_brand_specializations wbs
      where wbs.workshop_id = w.id
        and wbs.make_id = p_make_id
    ) desc,
    coalesce(s.bayesian_score, 0) desc,
    coalesce(s.verified_review_count, 0) desc,
    extensions.st_distance(w.location, qp.location) asc
  limit least(greatest(coalesce(p_limit, 50), 1), 100)
  offset greatest(coalesce(p_offset, 0), 0);
$$;

create or replace function public.workshops_in_view(
  p_min_lat double precision,
  p_min_lng double precision,
  p_max_lat double precision,
  p_max_lng double precision
)
returns table (
  id uuid,
  name text,
  slug text,
  latitude double precision,
  longitude double precision,
  bayesian_score numeric,
  review_count integer
)
language sql
stable
as $$
  select
    w.id,
    w.name,
    w.slug,
    extensions.st_y(w.location::extensions.geometry) as latitude,
    extensions.st_x(w.location::extensions.geometry) as longitude,
    s.bayesian_score,
    coalesce(s.review_count, 0) as review_count
  from public.workshops w
  left join public.workshop_review_stats s on s.workshop_id = w.id
  where w.status = 'published'
    and w.location::extensions.geometry && extensions.st_makeenvelope(
      p_min_lng,
      p_min_lat,
      p_max_lng,
      p_max_lat,
      4326
    );
$$;

