-- Reviews use 0-10 scores, matching the product brief.
create table public.reviews (
  id uuid primary key default extensions.gen_random_uuid(),
  workshop_id uuid not null references public.workshops(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  user_vehicle_id uuid references public.user_vehicles(id) on delete set null,
  service_category_id bigint references public.service_categories(id),
  service_date date,
  score_service smallint not null check (score_service between 0 and 10),
  score_treatment smallint not null check (score_treatment between 0 and 10),
  score_knowledge smallint not null check (score_knowledge between 0 and 10),
  score_price_value smallint not null check (score_price_value between 0 and 10),
  score_would_return smallint not null check (score_would_return between 0 and 10),
  overall_score numeric(4,2) generated always as (
    (
      score_service +
      score_treatment +
      score_knowledge +
      score_price_value +
      score_would_return
    ) / 5.0
  ) stored,
  comment text,
  verified_visit boolean not null default false,
  status text not null default 'published'
    check (status in ('pending', 'published', 'hidden', 'rejected')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

